import { PROMPT_FIELDS, SYSTEM_PROMPTS } from './prompts';

function escapeXml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function escapeJsonInXml(str: string): string {
  return str.replace(/\n/g, '\\n').replace(/"/g, '\\"');
}

export function exportFieldAsXmlAiConfig(
  fieldId: string,
  prompt: string,
  variant: 'old' | 'new'
): string {
  const escapedPrompt = escapeJsonInXml(prompt);
  return `        <aiConfig>
          <id>default</id>
          <prompt>
            {
            "messages": [
            {
            "role": "user",
            "content": "${escapedPrompt}"
            }
            ]
            }
          </prompt>
        </aiConfig>`;
}

export function exportSuggestionAiConfig(
  variant: 'old' | 'new',
  provider: string = 'academiccloud',
  model: string = 'gemma-4-31b-it'
): string {
  const system = variant === 'old' ? SYSTEM_PROMPTS.old : SYSTEM_PROMPTS.new;
  const sysEscaped = escapeJsonInXml(system);

  const userContent = variant === 'old'
    ? `Ich benötige Hilfe bei diesem Material. Es hat die folgenden Eigenschaften \\nTitel: {{var(cclom:title)|-}}\\nDateiname: {{var(cm:name)|-}}\\nLink: {{var(ccm:wwwurl)|-}}\\nMaterialart: {{var(ccm:educationallearningresourcetype)|-}}\\nSchlagworte: {{var(cclom:general_keyword)|-}}\\nBeschreibung: {{var(cclom:general_description)|-}}\\nFormat: {{node(cclom:format)|-}}\\nMedientyp: {{node(virtual:mediatype)|-}}\\n\\nVolltext/Inhalt:\\n{{node(textContent)|-}}`
    : `Material mit folgenden vorhandenen Informationen:\\nTitel: {{var(cclom:title)|-}}\\nBeschreibung: {{var(cclom:general_description)|-}}\\nSchlagworte: {{var(cclom:general_keyword)|-}}\\nDateiname: {{var(cm:name)|-}}\\nLink: {{var(ccm:wwwurl)|-}}\\nÜbergeordnete Kategorie: {{var(ccm:oeh_extendedType)|-}}\\nMaterialart: {{var(ccm:educationallearningresourcetype)|-}}\\nInhaltstyp: {{var(ccm:oeh_lrt)|-}}\\nBildungsstufe: {{node(ccm:educationalcontext)|-}}\\nFachgebiet: {{node(ccm:taxonid)|-}}\\nZielgruppe: {{var(ccm:educationalintendedenduserrole)|-}}\\nBerufsgruppe: {{var(ccm:oeh_profession_group)|-}}\\nSprache: {{node(cclom:general_language)|-}}\\nFormat: {{node(cclom:format)|-}}\\nMedientyp: {{node(virtual:mediatype)|-}}\\n\\nVolltext:\\n{{node(textContent)|-}}`;

  return `    <aiConfig>
      <id>suggestion_ai</id>
      <provider>${escapeXml(provider)}</provider>
      <useCaching>false</useCaching>
      <prompt>
        {
        "model": "${escapeXml(model)}",
        "messages": [
        {
        "role":"system",
        "content": "${sysEscaped}"
        },
        {
        "role": "user",
        "content": "${userContent}"
        }
        ]
        }
      </prompt>
    </aiConfig>`;
}

export function exportFullMdsXml(
  variant: 'old' | 'new',
  customPrompts: Record<string, string>,
  provider: string = 'academiccloud',
  model: string = 'gemma-4-31b-it'
): string {
  const fields = PROMPT_FIELDS;

  const widgetConfigs = fields
    .filter(f => ['cclom:title', 'cclom:general_description', 'cclom:general_keyword',
      'ccm:educationallearningresourcetype'].includes(f.id))
    .map(f => {
      const prompt = customPrompts[f.id] || (variant === 'new' ? f.newPrompt : f.oldPrompt);
      return { id: f.id, prompt };
    });

  let xml = `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by WLO Prompt Tester - ${variant === 'new' ? 'Optimierte' : 'Original'} Prompts -->
<metadataset id="default" label="default" hidden="false">
  <name>Core Metadataset</name>
  <i18n>mds</i18n>

  <aiConfigs>
${exportSuggestionAiConfig(variant, provider, model)}
  </aiConfigs>

  <!-- Widget aiConfigs (Auszug) -->
`;

  for (const wc of widgetConfigs) {
    xml += `  <!-- ${wc.id} -->
  <!--
${exportFieldAsXmlAiConfig(wc.id, wc.prompt, variant)}
  -->

`;
  }

  xml += `</metadataset>`;
  return xml;
}
